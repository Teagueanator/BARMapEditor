# Why a script (not build.rs): same as fetch-feature-decals.sh + the
# others — build.rs network access is hostile to offline builds, CI
# without network, and reproducible packaging.
#
# Why no in-repo redistribution: upstream mapfeatures has AI_POLICY.md
# but NO LICENSE file. We do not vendor models inside this repo or
# any built artifact; the user opts in by running this script
# locally. tools/feature-s3o/ is gitignored.
#
# Idempotent: per-entry files are skipped if already installed. Re-runs
# after `git pull` upstream pick up the freshest model only when

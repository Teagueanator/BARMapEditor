gitignored — these models never enter the BAR Map Editor repo or
any release artifact. The user opts in by running this script.

EOF
            exit 0
            ;;
        *)
            printf 'fetch-feature-s3o: unknown flag: %s (try --help)\n' "$1" >&2
            exit 2
            ;;
    esac
    shift
done

# ---- ensure upstream clone -------------------------------------------------
ensure_upstream() {
    if [ -d "$UPSTREAM_DIR/.git" ]; then
        return 0
    fi
    if [ "$MODE" = 'check' ]; then
        printf 'fetch-feature-s3o: upstream clone missing at %s\n' \
            "$UPSTREAM_DIR" >&2
        printf '  run "%s" without --check to clone it.\n' "$0" >&2
        exit 1
    fi
    printf 'fetch-feature-s3o: cloning %s -> %s (shallow)\n' \
        "$UPSTREAM_URL" "$UPSTREAM_DIR"
    mkdir -p "$(dirname "$UPSTREAM_DIR")"
    git clone --depth 1 "$UPSTREAM_URL" "$UPSTREAM_DIR"
}

warn_on_pin_drift() {
    local head
    head="$(git -C "$UPSTREAM_DIR" rev-parse HEAD 2>/dev/null || echo unknown)"
    if [ "$head" != "$PINNED_COMMIT" ]; then
        printf '  note: upstream HEAD is %s (pin was %s); mapping may have drifted.\n' \
            "${head:0:8}" "${PINNED_COMMIT:0:8}"
    fi
}

ensure_upstream
warn_on_pin_drift

# ---- check mode ------------------------------------------------------------
if [ "$MODE" = 'check' ]; then
    fail=0
    for entry in "${ENTRIES[@]}"; do
        name="${entry%%:*}"
        path="${entry#*:}"
        src="$UPSTREAM_DIR/objects3d/$path"
        dst="$DEST_ROOT/$name.s3o"
        if [ ! -f "$src" ]; then
            printf '  [%s] UPSTREAM MISSING: %s\n' "$name" "$src" >&2
            fail=1
            continue
        fi
        if [ -f "$dst" ] && [ ! -s "$dst" ]; then
            printf '  [%s] LOCAL EMPTY: %s (re-run without --check)\n' \
                "$name" "$dst" >&2
            fail=1
            continue
        fi
    done
    if [ "$fail" -ne 0 ]; then
        printf 'fetch-feature-s3o: --check failed\n' >&2
        exit 1
    fi
    printf 'fetch-feature-s3o: --check ok (%d entries advertised)\n' "${#ENTRIES[@]}"
    exit 0
fi

# ---- install / refresh -----------------------------------------------------
mkdir -p "$DEST_ROOT"

installed=0
skipped=0
missing=0

for entry in "${ENTRIES[@]}"; do
    name="${entry%%:*}"
    path="${entry#*:}"
    src="$UPSTREAM_DIR/objects3d/$path"
    dst="$DEST_ROOT/$name.s3o"

    if [ ! -f "$src" ]; then
        printf '  [%s] UPSTREAM MISSING %s\n' "$name" "$src" >&2
        missing=$((missing + 1))
        continue
    fi

    if [ -f "$dst" ] && [ "$MODE" != 'refresh' ]; then
        skipped=$((skipped + 1))
        continue
    fi

    cp "$src" "$dst"
    installed=$((installed + 1))
done

printf 'fetch-feature-s3o: %d installed, %d skipped, %d missing (of %d advertised).\n' \
    "$installed" "$skipped" "$missing" "${#ENTRIES[@]}"

if [ "$missing" -gt 0 ]; then
    printf '  %d entries lack an upstream .s3o — runtime falls back\n' \
        "$missing" >&2
    printf '  to the Phase A decal sprite for those.\n' >&2
fi
